public class Calculator {

    public int add(int int1, int int2) {
        return int1 + int2;
    }

    public int subtract(int int1, int int2) {
        return int1 - int2;
    }

    public int multiply(int int1, int int2) {
        return int1 * int2;
    }

    public double divide(double one, double two) {
        return one / two;
    }

}
